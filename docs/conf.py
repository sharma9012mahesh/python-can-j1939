# Auto-generate examples.rst from all .py files found under examples/.
def _generate_examples_rst():
    examples_dir = HERE.parent / 'examples'
    lines = [
        'Examples',
        '========',
        '',
        'Example scripts demonstrating how to use the python-can-j1939 library.',
        '',
    ]
    for path in sorted(examples_dir.rglob('*.py')):
        rel = path.relative_to(HERE.parent)
        title = path.stem.replace('_', ' ').title()
        lines += [
            title,
            '-' * len(title),
            '',
            f'.. literalinclude:: ../{rel.as_posix()}',
            '   :language: python',
            f'   :caption: {path.name}',
            '',
        ]
    (HERE / 'examples.rst').write_text('\n'.join(lines), encoding='utf-8')

_generate_examples_rst()

